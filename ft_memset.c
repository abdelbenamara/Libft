#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	const uint8_t	b = c;
	uint8_t			*r;

	r = s;
	while (n)
	{
		*r = b;
		++r;
		--n;
	}
	return (s);
}
